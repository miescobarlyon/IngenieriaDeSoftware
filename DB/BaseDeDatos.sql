CREATE PROCEDURE [dbo].[InsertarBitacora]
    @usuario INT,
    @fecha DATETIME,
    @actividad VARCHAR(50),
    @criticidad INT
AS
BEGIN
    DECLARE @id int
    SET @id = (SELECT ISNULL(MAX(BITACORA_ID), 0) + 1 FROM BITACORA)

    INSERT INTO Bitacora (BITACORA_ID, USUARIO_ID, Fecha, Actividad, CRITICIDAD_ID)
    VALUES (@id, @usuario, @fecha, @actividad, @criticidad)
END
GO
