CREATE PROCEDURE [dbo].[ListarBitacoraFiltrado]
    @usuarioId INT = NULL,
    @fechaInicio DATETIME = NULL,
    @fechaFin DATETIME = NULL,
    @criticidad INT = NULL,
    @actividad NVARCHAR(255) = NULL
AS
BEGIN
    SELECT BITACORA_ID, USUARIO_ID, FECHA, ACTIVIDAD, CRITICIDAD_ID
    FROM Bitacora
    WHERE 
        (@usuarioId IS NULL OR USUARIO_ID = @usuarioId)
        AND (@fechaInicio IS NULL OR Fecha >= @fechaInicio)
        AND (@fechaFin IS NULL OR Fecha <= @fechaFin)
        AND (@criticidad IS NULL OR CRITICIDAD_ID = @criticidad)
        AND (@actividad IS NULL OR Actividad LIKE '%' + @actividad + '%')
    ORDER BY Fecha DESC
END
GO
